SinglyLinkedListNode* reverse(SinglyLinkedListNode* head) {
    if (!head) {
        return head;
    }
    vector<SinglyLinkedListNode*> order;

    while (head) {
        order.push_back(head);
        head = head->next;
    }
    
    for (int i = (int)order.size() - 1; i > 0; i--) {
        order[i]->next = order[i - 1];
    }
    order[0]->next = nullptr;
    return order.back();
}

